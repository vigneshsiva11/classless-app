// Helper function to get content by grade
export function getContentByGrade(grade: number): DocChunk[] {
  return educationalContent.filter(
    (content) => content.metadata?.grade === grade
  );
}

// Helper function to get content by subject
export function getContentBySubject(subject: string): DocChunk[] {
  return educationalContent.filter(
    (content) => content.metadata?.subject === subject
  );
}

// Helper function to get content by grade and subject
export function getContentByGradeAndSubject(
  grade: number,
  subject: string
): DocChunk[] {
  return educationalContent.filter(
    (content) =>
      content.metadata?.grade === grade && content.metadata?.subject === subject
  );
}

// Helper function to get all available grades
export function getAvailableGrades(): number[] {
  const grades = new Set(
    educationalContent.map((content) => content.metadata?.grade).filter(Boolean)
  );
  return Array.from(grades).sort((a, b) => a - b);
}

// Helper function to get all available subjects
export function getAvailableSubjects(): string[] {
  const subjects = new Set(
    educationalContent
      .map((content) => content.metadata?.subject)
      .filter(Boolean)
  );
  return Array.from(subjects).sort();
}

// Helper function to get content statistics
export function getContentStatistics(): {
  totalContent: number;
  grades: { [grade: number]: number };
  subjects: { [subject: string]: number };
} {
  const grades: { [grade: number]: number } = {};
  const subjects: { [subject: string]: number } = {};

  educationalContent.forEach((content) => {
    const grade = content.metadata?.grade;
    const subject = content.metadata?.subject;

    if (grade) {
      grades[grade] = (grades[grade] || 0) + 1;
    }

    if (subject) {
      subjects[subject] = (subjects[subject] || 0) + 1;
    }
  });

  return {
    totalContent: educationalContent.length,
    grades,
    subjects,
  };
}
